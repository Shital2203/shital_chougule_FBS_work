# Distance Conversion: Kilometers to Meters and Centimeters


km = float(input("Enter distance in kilometers: "))


meters = km * 1000
centimeters = km * 100000

print("\n--- Distance Conversion ---")
print(f"Distance in Kilometers : {km:.2f} km")
print(f"Distance in Meters     : {meters:.2f} m")
print(f"Distance in Centimeters: {centimeters:.2f} cm")
